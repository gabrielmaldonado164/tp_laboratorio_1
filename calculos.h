#include <stdio.h>

/** \brief Realiza la suma de los numeros decimales ingresados
 *
 * \param float El valor del primer numero que se ingresa
 * \param float El valor del segundo numero que se ingresa
 * \return float Resultado de la suma de los numeros ingresados
 *
 */
float sumar(float ,float );
/** \brief Realiza la resta de los numeros decimales ingresados
 *
 * \param float El valor del primer numero que se ingresa
 * \param float El valor del segundo numero que se ingresa
 * \return float Resultado de la resta de los numeros ingresados
 *
 */
float restar(float, float);
/** \brief  Realiza la multiplicacion de los numeros ingresados
 *
 * \param float El valor del primer numero que se ingresa
 * \param float El valor del segundo numero que se ingresa
 * \return float Resultado de la multiplicacion de los numeros ingresados
 *
 */
float multiplicar(float,float);
/** \brief Realiza la division de los numeros ingresados
 *
 * \param float El valor del primer numero que se ingresa
 * \param float El valor del segundo numero que se ingresa
 * \return float Resultado de la division de los numeros ingresados
 *
 */
float division(float,float);
/** \brief Realiza el factorial de un numero entero
 *
 * \param int El valor del numero entero
 * \return int Resultado factorial del numero entero ingresado
 *
 */
int factorial(int);
